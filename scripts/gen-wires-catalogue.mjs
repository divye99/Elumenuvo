/**
 * Generate the expanded house-wire catalogue from the July-2026 brand list prices
 * (KEI 20 Apr 2026, Polycab 1 Jun 2026, APAR 21 May 2026, Havells live). Emits a
 * migration: one variant family per (brand, grade, size) with 6 colour variants.
 *   node scripts/gen-wires-catalogue.mjs → supabase/migrations/0024_catalogue-wires.sql
 *
 * MRP = each brand's own list price per 90 m coil (research-sourced, base grade
 * × grade uplift). Selling price = MRP − 46% placeholder (radar refines later).
 * Colour doesn't affect price; grade does. Single-core, 90 m coil, IS 694.
 */
import fs from "fs";

const q = (s) => `'${String(s).replace(/'/g, "''")}'`;
const SELL = 0.54; // launch placeholder = 46% off MRP

// ── Base-grade MRP per 90 m coil, ₹ (from the latest brand list prices) ──
const BASE = {
  Havells: { grade0: "HRFR", 0.5: 1905, 0.75: 2685, 1.0: 3645, 1.5: 5345, 2.5: 8485, 4.0: 12435, 6.0: 18510 },
  KEI:     { grade0: "FR",   0.5: 1773, 0.75: 2385, 1.0: 3078, 1.5: 4725, 2.5: 7668, 4.0: 11448, 6.0: 17478 },
  Polycab: { grade0: "FRLS", 0.5: 1836, 0.75: 2493, 1.0: 3402, 1.5: 4991, 2.5: 8348, 4.0: 12870, 6.0: 19188 },
  APAR:    { grade0: "HR-FR",0.5: 1827, 0.75: 2493, 1.0: 3186, 1.5: 4716, 2.5: 7515, 4.0: 11367, 6.0: 16812 },
};
const SIZES = [0.5, 0.75, 1.0, 1.5, 2.5, 4.0, 6.0];

// ── Grades per brand: display name + MRP multiplier over the base grade ──
const GRADES = {
  Havells: [["Life Line Plus S3 HRFR", "HRFR", 1.0], ["Life Guard FR-LSH", "FR-LSH", 1.03]],
  KEI:     [["Homecab FR", "FR", 1.0], ["Homecab HRFR", "HRFR", 1.04], ["Homecab FRLSH", "FRLSH", 1.04], ["Homecab HFFR", "HFFR", 1.05]],
  Polycab: [["GreenWire FR", "FR", 0.96], ["GreenWire FRLS", "FRLS", 1.0], ["GreenWire FRLSH", "FRLSH", 1.02]],
  APAR:    [["Anushakti FR-PVC", "FR-PVC", 0.95], ["Anushakti HR-FR-PVC", "HR-FR", 1.0], ["Anushakti HR-FR-LSH", "HR-FR-LSH", 1.03]],
};
// Distinct 2-letter colour codes (no collisions) for SKUs.
const COLOURS = [["Red", "RD"], ["Yellow", "YL"], ["Blue", "BU"], ["Black", "BK"], ["Green", "GN"], ["Grey", "GY"]];
const REP_SIZE = 2.5; // the variant shown on the catalogue card by default (most common)

const sizeKey = (s) => String(s).replace(".", "p");
const abbr = { Havells: "HAV", KEI: "KEI", Polycab: "POL", APAR: "APR" };

// One family per (brand, grade); size + colour are BOTH variant dimensions, so a
// single card shows size chips AND colour swatches. Parent = (2.5 sq mm, Red).
const rows = [];
let sort = 100;
for (const brand of Object.keys(BASE)) {
  for (const [gradeName, gradeCode, mult] of GRADES[brand]) {
    const gk = gradeCode.replace(/[^A-Za-z0-9]/g, "").toLowerCase();
    const family = `wire-${brand.toLowerCase()}-${gk}`;
    const repId = `${family}-${sizeKey(REP_SIZE)}-rd`;
    for (const size of SIZES) {
      const mrp = Math.round(BASE[brand][size] * mult);
      const sell = Math.round(mrp * SELL);
      for (const [colour, cc] of COLOURS) {
        const id = `${family}-${sizeKey(size)}-${cc.toLowerCase()}`;
        rows.push({
          id,
          parent_id: id === repId ? null : repId,
          sku: `${abbr[brand]}-${gradeCode.replace(/[^A-Za-z0-9]/g, "")}-${size}-${cc}`,
          name: `${brand} ${gradeName} ${size} sq mm House Wire — ${colour}`,
          brand,
          spec: `${size} sq mm · single core · 90 m coil · ${gradeCode} · IS 694`,
          mrp,
          sell,
          attrs: { Size: `${size} sq mm`, Colour: colour, Grade: gradeCode, Length: "90 m" },
          sort: sort++,
        });
      }
    }
  }
}
// Emit parents first so child parent_id FKs resolve during the migration.
rows.sort((a, b) => (a.parent_id === null ? -1 : 0) - (b.parent_id === null ? -1 : 0) || a.sort - b.sort);

const lines = [
  "-- ═══════════════════════════════════════════════════════════════",
  "-- House-wire catalogue expansion (generated by scripts/gen-wires-catalogue.mjs).",
  "-- MRP = brand list price per 90 m coil — KEI 20 Apr 2026, Polycab 1 Jun 2026,",
  "-- APAR 21 May 2026, Havells (live). Selling = MRP − 46% placeholder; refine via",
  "-- the price radar. Idempotent. One variant family per brand/grade/size × 6 colours.",
  `-- ${rows.length} products across ${Object.keys(BASE).length} brands.`,
  "-- ═══════════════════════════════════════════════════════════════",
  "",
];
for (const r of rows) {
  lines.push(
    `insert into public.products (id, sku, name, brand, category, spec, mrp, elume_price, unit, sort_order, is_active, parent_id, attrs) values (` +
      [q(r.id), q(r.sku), q(r.name), q(r.brand), q("Wires & Cables"), q(r.spec), r.mrp, r.sell, q("coil"), r.sort, "true", r.parent_id ? q(r.parent_id) : "null", `'${JSON.stringify(r.attrs).replace(/'/g, "''")}'::jsonb`].join(", ") +
      `)\n  on conflict (id) do update set mrp = excluded.mrp, name = excluded.name, spec = excluded.spec, attrs = excluded.attrs;`,
    `insert into public.price_history (product_id, elume_price, mrp) select ${q(r.id)}, ${r.sell}, ${r.mrp} where not exists (select 1 from public.price_history where product_id = ${q(r.id)});`
  );
}
lines.push("", `select brand, count(*) from public.products where category = 'Wires & Cables' group by brand order by brand;`, "");

fs.writeFileSync("supabase/migrations/0024_catalogue-wires.sql", lines.join("\n"));
console.log(`Wrote ${rows.length} wire products → supabase/migrations/0024_catalogue-wires.sql`);
const byBrand = {};
for (const r of rows) byBrand[r.brand] = (byBrand[r.brand] || 0) + 1;
console.log(byBrand);
